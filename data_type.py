# different data types in pyhton

# Integer
a = 10
print("Value of a:", a, " Data Type:", type(a))

# Float
b = 3.14
print("Value of b:", b, " Data Type:", type(b))

# String
c = "Hello, World!"
print("Value of c:", c, " Data Type:", type(c))

# Boolean
d = True
print("Value of d:", d, " Data Type:", type(d))

# List
e = [1, 2, 3, 4, 5]
print("Value of e:", e, " Data Type:", type(e))

# Tuple
f = (10, 20, 30)
print("Value of f:", f, " Data Type:", type(f))

# Set
g = {100, 200, 300}
print("Value of g:", g, " Data Type:", type(g))

# Dictionary
h = {"name": "Alice", "age": 25}
print("Value of h:", h, " Data Type:", type(h))
