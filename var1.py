# Variable examples in Python

# Storing a number
age = 25

# Storing a string (text)
name = "Neha"

# Storing a floating point number
height = 5.6

# Storing a boolean value (True/False)
is_student = True

# Printing variables
print(name)
print("Age:", age)
print("Height:", height)
print("Is student?", is_student)
